import Anthropic from "@anthropic-ai/sdk";
import type { PressRelease } from "./rss";

const anthropic = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY!,
});

/**
 * プレスリリースの内容からX投稿文を生成する
 * フォーマット: タイトル要約 + 概要 + 一言コメント（URL無し）
 * 140文字（全角）以内に収める
 */
export async function generatePost(pr: PressRelease): Promise<string> {
  const message = await anthropic.messages.create({
    model: "claude-haiku-4-5-20251001",
    max_tokens: 300,
    messages: [
      {
        role: "user",
        content: `あなたはアイスクリーム評論家「アイスマン福留」としてXに投稿します。
以下のプレスリリースから、新商品紹介の投稿文を作成してください。

【投稿フォーマット】
【新商品】メーカー名＋商品名＋発売日＋販売エリア

商品の特徴を1〜3文で紹介（味・素材・構造など具体的に）。価格情報があれば含める。

最後にアイスマン福留らしい一言（ファンへの呼びかけ、期待感、食べたいポイントなど自然なトーン）

【最重要ルール】
このプレスリリースが「新商品の発売告知」でない場合（例：キャンペーン情報、イベント開催告知、企業・決算・採用情報、既存商品のリニューアル告知以外の内容、アイスクリーム以外の商品など）は、本文の代わりに「SKIP」とだけ出力してください。それ以外の文字は一切出力しないでください。

【投稿ルール】（新商品の場合のみ適用）
- 冒頭は必ず「【新商品】」から始める
- メーカー名（ロッテ、森永乳業、赤城乳業など）を明記
- 発売日・価格・販売エリアなど具体情報をプレスリリースから正確に抽出する
- プレスリリースに記載がない情報は絶対に捏造しない
- URLは絶対に含めない
- ハッシュタグは不要
- 絵文字は使わない
- 全体で280文字（半角換算）以内に収める
- 自然で読みやすい日本語、カジュアルだけど情報量がある文体

【参考例】
【新商品】ロッテより、人気の「爽」シリーズから『爽 ブルーベリーヨーグルト味』が2026年4月13日（月）から全国にて発売！  ブルーベリーアイスとプレーンヨーグルト味のすっきりしたアイスの組み合わせが相性抜群。ぐるぐるな2色巻き仕様で価格は194円（税込)

【新商品】人気アイス「パルム」から新作が登場！  森永乳業「PARM（パルム） 白桃＆アールグレイ」4月20日（月）から全国で期間限定発売!!   アールグレイミルクティーアイスを、ホワイトチョコでコーティング。さらに、アイスの中には甘くジューシーな白桃ソースをうずまき状に加え、本格スイーツのような味わいを実現。価格は180円（税別）パルムファンの皆さま、お楽しみにー！

【プレスリリース】
タイトル: ${pr.title}
内容: ${pr.description}

投稿文のみを出力してください。余計な説明や前置きは不要です。`,
      },
    ],
  });

  const text =
    message.content[0].type === "text" ? message.content[0].text : "";
  return text.trim();
}

/**
 * プレスリリースから発売日を抽出する（YYYY-MM-DD形式）
 * 発売日が特定できない場合は null を返す
 */
export async function extractReleaseDate(
  pr: PressRelease
): Promise<string | null> {
  try {
    const message = await anthropic.messages.create({
      model: "claude-haiku-4-5-20251001",
      max_tokens: 50,
      messages: [
        {
          role: "user",
          content: `以下のプレスリリースから商品の「発売日」を抽出してください。

【ルール】
- YYYY-MM-DD 形式で出力（例: 2026-04-13）
- 発売日が明確に記載されている場合のみ出力
- 「発売中」「好評発売中」など既に発売済みの場合は「NONE」と出力
- 発売日が不明な場合は「NONE」と出力
- 日付のみを出力。余計な文字は不要

タイトル: ${pr.title}
内容: ${pr.description}`,
        },
      ],
    });

    const result =
      message.content[0].type === "text" ? message.content[0].text.trim() : "";

    // YYYY-MM-DD形式の日付かチェック
    if (/^\d{4}-\d{2}-\d{2}$/.test(result)) {
      return result;
    }
    return null;
  } catch (error) {
    console.error("発売日抽出エラー:", error);
    return null;
  }
}

